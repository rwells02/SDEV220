#3.1/3.2
secInMin = 60
minInHour = 60
secPerHour = secInMin * minInHour
print("Seconds per hour:", secPerHour)

#3.3/3.4
hoursPerDay = 24
secPerDay = secPerHour * hoursPerDay
print("Seconds Per Day:", secPerDay)

#3.5
floatingDivResult =  secPerDay / secPerHour
print("Floating-point division:", floatingDivResult)

#3.6
integerDivResult = secPerDay // secPerHour
print("Integer division:", integerDivResult)

